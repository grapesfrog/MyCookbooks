service "Mongodb" do
  service_name "mongod"
  case node["platform"]
  when "centos","redhat","fedora","amazon"
    supports :restart => true, :status => true
  when "debian","ubuntu"
    supports :restart => true, :reload => true, :status => true
  end
  action [:enable, :start]
end
